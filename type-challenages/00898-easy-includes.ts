type Includes<T extends readonly any[], U> = T extends [infer First, ...infer Rest]
? Equal<First, U> extends true // 如果第一个元素与 U 匹配
  ? true // 返回 true
  : Includes<Rest, U> // 否则递归检查剩下的元素
: false;